using System;
namespace C1.consoleApp.Week1
{
    public class Calculator
    {
        public Calculator()
        {
        }
        public int add(int a, int b)
        {
            return a + b;
        }
        public int sub(int a, int b)
        {
            return a - b;
        }
        public int multiplication(int a, int b)
        {
            return a * b;

        }
        public int div(int a, int b)
        {
            return b / a;
        }
    }


}


